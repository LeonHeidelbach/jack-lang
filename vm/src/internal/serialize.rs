use super::parse::Program;

use shared::util::traits::Serializable;

////////////////////////////////////////////////////////////////////////////////
// Program Serialization
////////////////////////////////////////////////////////////////////////////////

impl<'a> Serializable for Program<'a> {
  type Output = String;
  fn serialize(&self) -> String {
    return self.instructions.clone();
  }
}
